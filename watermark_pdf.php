    }
    
    /**
     * Decompress PDF using Ghostscript for FPDI compatibility
     */
    private function decompressPdf($inputPdf) {
        $decompressed = $this->outputDir . '/decomp_' . md5($inputPdf) . '.pdf';
        
        // Cache: reuse if already decompressed and source hasn't changed
        if (file_exists($decompressed) && filemtime($decompressed) >= filemtime($inputPdf)) {
            return $decompressed;
        }
        
        $gsPath = '/usr/bin/gs';
        if (!file_exists($gsPath)) {
            return null;
        }
        
        $cmd = escapeshellcmd($gsPath) 
            . ' -dBATCH -dNOPAUSE -dQUIET -sDEVICE=pdfwrite -dCompatibilityLevel=1.4'
            . ' -sOutputFile=' . escapeshellarg($decompressed) 
            . ' ' . escapeshellarg($inputPdf);
        
        // Use proc_open since exec/shell_exec are disabled on shared hosting
        $descriptors = [
            0 => ['pipe', 'r'],
            1 => ['pipe', 'w'],
            2 => ['pipe', 'w'],
        ];
        
        $process = proc_open($cmd, $descriptors, $pipes);
        if (!is_resource($process)) {
            error_log('Ghostscript: proc_open failed');
            return null;
        }
        
        fclose($pipes[0]);
        $stdout = stream_get_contents($pipes[1]);
        $stderr = stream_get_contents($pipes[2]);
        fclose($pipes[1]);
        fclose($pipes[2]);
        $returnCode = proc_close($process);
        
        if ($returnCode !== 0 || !file_exists($decompressed)) {
            error_log('Ghostscript decompress failed (code ' . $returnCode . '): ' . $stderr);
            return null;
        }
        
        return $decompressed;
    }
    
    /**
     * Apply watermark using Ghostscript + FPDI
     */
    private function applyWatermarkWithFpdi($inputPdf, $outputPdf, $buyerName, $buyerEmail) {
        try {
            // Decompress PDF first for FPDI compatibility
            $sourcePdf = $this->decompressPdf($inputPdf);
            if (!$sourcePdf) {
                $sourcePdf = $inputPdf; // Try original if gs fails
            }
            
            $pdf = new Fpdi();
            $pdf->SetAutoPageBreak(false);
            $pageCount = $pdf->setSourceFile($sourcePdf);
            $year = date('Y');
            $watermarkText = iconv('UTF-8', 'ISO-8859-1//TRANSLIT', "(c) {$year} {$buyerName} | {$buyerEmail}");
            
            for ($pageNo = 1; $pageNo <= $pageCount; $pageNo++) {
                $templateId = $pdf->importPage($pageNo);
                $size = $pdf->getTemplateSize($templateId);
                
                $pdf->AddPage($size['orientation'], [$size['width'], $size['height']]);
                $pdf->useTemplate($templateId);
                
                // Add watermark at bottom center
                $pdf->SetFont('Arial', '', 9);
                $pdf->SetTextColor(128, 128, 128);
                $pdf->SetXY(10, $size['height'] - 12);
                $pdf->Cell($size['width'] - 20, 10, $watermarkText, 0, 0, 'C');
            }
            
            $pdf->Output($outputPdf, 'F');
            
            return [
                'success' => true,
                'method' => 'fpdi',
                'watermark_text' => "© {$year} {$buyerName} | {$buyerEmail}",
                'output_file' => $outputPdf,
                'page_count' => $pageCount,
                'message' => 'Watermark applied successfully'
            ];
            
        } catch (\Throwable $e) {
            error_log('FPDI Watermark Error: ' . $e->getMessage());
            
            // Fallback to info file
            return $this->createWatermarkInfoFile($inputPdf, $outputPdf, $buyerName, $buyerEmail, date('Y-m-d H:i:s'));
        }
    }
    
    /**
     * Get watermark text
     */
    public function getWatermarkText() {
        return $this->watermarkText;
    }
    
    /**
     * Clean up old temp files (older than 24 hours)
     */
    public function cleanupOldFiles() {
        $files = glob($this->outputDir . '/*');
        $now = time();
        
        foreach ($files as $file) {
            if (is_file($file)) {
                if ($now - filemtime($file) >= 86400) { // 24 hours
                    unlink($file);
                }
            }
        }
    }
}
