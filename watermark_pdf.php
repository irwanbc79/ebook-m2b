/**
 * M2B E-book PDF Watermark Helper
 * Generates personalized watermarked PDFs per buyer using FPDI
 * 
 * Updated: March 2026
 */

// Composer autoload (if not already loaded)
$autoloadPath = __DIR__ . '/vendor/autoload.php';
if (file_exists($autoloadPath) && !class_exists('setasign\Fpdi\Fpdi')) {
    require_once $autoloadPath;
}

use setasign\Fpdi\Fpdi;
use setasign\Fpdf\Fpdf;

class PDFWatermark {
    private $watermarkText;
    private $outputDir;
    
    public function __construct() {
        $this->outputDir = __DIR__ . '/temp';
        
        // Create temp directory if not exists
        if (!is_dir($this->outputDir)) {
            mkdir($this->outputDir, 0755, true);
        }
    }
    
    /**
     * Add watermark to PDF
     * On shared hosting, creates watermark info file instead
     */
    public function addWatermark($inputPdf, $outputPdf, $buyerName, $buyerEmail) {
        $timestamp = date('Y-m-d H:i:s');
        $year = date('Y');
        
        // Create watermark text
        $this->watermarkText = "© {$year} {$buyerName} | {$buyerEmail}";
        $ebookTitle = defined('EBOOK_TITLE') ? EBOOK_TITLE : 'Panduan Global Export & Import untuk Indonesia';
        
        // Try to use FPDI if available
        if (class_exists('setasign\Fpdi\Fpdi')) {
            return $this->applyWatermarkWithFpdi($inputPdf, $outputPdf, $buyerName, $buyerEmail);
        }
        
        // Fallback: Create watermark info file
        $watermarkInfo = $this->createWatermarkInfoFile($inputPdf, $outputPdf, $buyerName, $buyerEmail, $timestamp);
        
        // Copy original PDF to temp (for manual watermarking)
        if (file_exists($inputPdf)) {
            copy($inputPdf, $outputPdf);
        }
        
        return $watermarkInfo;
    }
    
    /**
     * Create watermark info file for manual application
     */
    private function createWatermarkInfoFile($inputPdf, $outputPdf, $buyerName, $buyerEmail, $timestamp) {
        $year = date('Y');
        $ebookTitle = defined('EBOOK_TITLE') ? EBOOK_TITLE : 'Panduan Global Export & Import untuk Indonesia';
        
        $infoContent = <<<INFO
